public class If_exam01 {

	public static void main(String[] args) {
		
		int score = 83;
		if(score >= 90) {
			System.out.println("점수가 90이상이네요");
			System.out.println("등급은 A입니다.");
		}
		if(score < 90) {
			System.out.println("점수가 90미만입니다.");
			System.out.println("A등급은 아니예요.");
		}
		
		

	}

}
